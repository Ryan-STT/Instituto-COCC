function saibamais1(){
    var pontos=document.getElementById("pontosjuh");
    var maistexto=document.getElementById("saibamaisjuh");
    var btnSaibaMais=document.getElementById("saibamais1");
    if(pontos.style.display === "none"){
        pontos.style.display="inline";
        maistexto.style.display="none";
        btnSaibaMais.innerHTML="Saiba Mais";
    }else{
        pontos.style.display="none";
        maistexto.style.display="inline";
        btnSaibaMais.innerHTML="Fechar"; 
    }
}
